//Operaciones con numeros 

const numero1 = 20;
const numero2 = 30;

let resultado;

// Suma
resultado = numero1 + numero2;
console.log(resultado);

// Resta
resultado = numero1 - numero2;
console.log(resultado);

// Multiplicacion
resultado = numero1 * numero2;
console.log(resultado);


//Division
resultado = numero1 / numero2;
console.log(resultado);


//Modulo
resultado = numero1 % numero2;
console.log(resultado);
